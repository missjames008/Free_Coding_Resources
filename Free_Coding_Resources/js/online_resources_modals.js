//Get the modal for Codecademy
var onlinemodal1 = document.getElementById('onlineModal1');

// Get the button that opens the modal
var onlinebtn1 = document.getElementById("onlineBtn1");
//
// // Get the <span> element that closes the modal
var onlinespan1 = document.getElementsByClassName("onlineclose1")[0];

// When the user clicks on the button, open the modal
onlinebtn1.onclick = function() {
    onlinemodal1.style.display = "block";
}
// When the user clicks on <span> (x), close the modal
onlinespan1.onclick = function() {
    onlinemodal1.style.display = "none";
}

